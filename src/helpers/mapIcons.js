function buildSVG(path, color) {
    return `<svg xmlns="http://www.w3.org/2000/svg" width="${ICON_SIZE}" height="${ICON_SIZE}" viewBox="0 0 16 16" fill="${color}" fill-rule="evenodd"><path d="${path}"/></svg>`;
}

function svgToImage(svgString) {
    return new Promise((resolve, reject) => {
        const img = new Image(ICON_SIZE, ICON_SIZE);
        img.onload = () => resolve(img);
        img.onerror = reject;
        img.src = `data:image/svg+xml;charset=utf-8,${encodeURIComponent(svgString)}`;
    });
}

export async function addPositionIcons(map) {
    const color = getColour("primary") || "#42c635";

    if (!map.hasImage("position")) {
        const img = await svgToImage(buildSVG(POSITION_PATH, color));
        map.addImage("position", img);
    }

    if (!map.hasImage("position-heading")) {
        const img = await svgToImage(buildSVG(POSITION_HEADING_PATH, color));
        map.addImage("position-heading", img);
    }
}
